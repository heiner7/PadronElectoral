    //Los predicados para los filtros de las busquedas
    public static Predicate <Padron> filtroCaducidad(String texto) {
        
            return p -> p.getCaducidad().equals(texto);     
    }
    
    public static Predicate <Padron> filtroApellido1(String texto) {
        
            return p -> p.getPrimerApe().startsWith(texto);     
    }
    
    public static Predicate <Padron> filtroApellido2(String texto) {
        
            return p -> p.getSegundoApe().startsWith(texto);     
    }
    
    public static Predicate <Padron> filtroCedula(String texto) {
        
            return p -> p.getCedula().equals(texto);     
    }
    
    public static Predicate <Padron> filtroNombre(String texto) {
        
            return p -> p.getNombre().startsWith(texto);     
    }
    
    //Filtro para la busqueda por cedula,nombre,apellido, caducidad y nombre completo
    public static List<Padron> filterPersonas(List<Padron> pacientes,
                                                Predicate<Padron> predicate)
    {
        return pacientes.stream()
                    .filter(predicate)
                    .collect(Collectors.<Padron>toList());
    }
    
    public void cargarDatos(List<Padron> padron)
    {   
             //Son las repeticiones que tiene que hacer para cargar los datos
             if(contador < padron.size()){
                 Padron p = padron.get(contador);
             
                 modelo.insertRow(contador,new Object[]{});
                 modelo.setValueAt(p.getCedula(),contador,0);
                 
                 if("1".equals(p.getGenero())){
                     modelo.setValueAt("Hombre",contador,1);
                 }else{
                     modelo.setValueAt("Mujer",contador,1);
                 }
                 
                 modelo.setValueAt(p.getCaducidad(),contador,2);
                 modelo.setValueAt(p.getNombre(),contador,3);
                 modelo.setValueAt(p.getPrimerApe(),contador,4);
                 modelo.setValueAt(p.getSegundoApe(), contador, 5);
                 
                 if(p.getProvincia().startsWith("6")){
                    modelo.setValueAt("Puntarenas", contador, 6);
                 }
                 contador++;
                 cargarDatos(padron);
             }
    }// registro de datos del Padron en la tabla
    
