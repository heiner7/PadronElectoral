    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
            if(checkNombre.isSelected()){
                
                padron = filterPersonas(Padron, filtroNombre(txtBuscar.getText().trim().toUpperCase()));
                
            }
            else if(checkCedula.isSelected()){
                padron = filterPersonas(Padron, filtroCedula(txtBuscar.getText().trim()));
            }
            else if(checkApellido.isSelected()){
                padron = filterPersonas(Padron, filtroApellido1(txtBuscar.getText().toUpperCase().trim()));
            }
            else if(checkCaducidad.isSelected()){
                padron = filterPersonas(Padron, filtroCaducidad(txtBuscar.getText().trim()));
            }
            else if(checkCompleto.isSelected()){
                              
               //Se pasa la frase a una variable para hecer el cambio de espacios
               textoBuscar = txtBuscar.getText().trim();
               //Se reemplaza los espacios que tenga m√°s de 2 a un solo espacio por palabra
               textoBuscar = textoBuscar.replaceAll(" {2,}", " ");
                //Aqui se separa el texto
                String[] informacion = textoBuscar.split(" ");
                nombre = informacion[0];
                apellido1 = informacion[1];
                apellido2 = informacion[2];
                padron = filterPersonas(Padron, (filtroNombre(nombre.trim().toUpperCase()).
                         and(filtroApellido1(apellido1.toUpperCase())).and(filtroApellido2(apellido2.toUpperCase()))));
               //Cantidad de nombre completos que se encontraron
                txtCantidad.setText(String.valueOf(padron.size()));
            }
                txtBuscar.setText(" ");
                //La cantidad de resultado en los filtros utilizado
                txtEncontrado.setText(String.valueOf(padron.size()));
                CargaInterFaz();
                cargarDatos(padron);
                contador=0;//Contador se vuelve cero para comenzar el recorrido de la lista al principio
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El dato no se encuentra en la Base");
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

